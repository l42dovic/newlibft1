#include "libft.h"
#include <stdio.h>
#include <limits.h>
#include <string.h>
#include <bsd/string.h>

void	ft_putstr_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	if (!s)
		return ;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
}

int main(void)
{
	char	str[] = "bidouille une nouille";

	printf("Tested function is : %s\n", "putstr_fd");
	ft_putstr_fd(str, 2);

	return (0);
}